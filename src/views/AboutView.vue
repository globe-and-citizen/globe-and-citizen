        <h3>Layer8</h3>
        Layer8 is being designed to accommodate the myriad of up-and-coming
        companies that want to expose their AI, or other services, online but
        will be held back by the liability of storing identifiable user data.
        Layer8 solves this problem by providing an opensource web proxy designed
        to anonymize and sanitize user data automatically. Makers of web
        applications and chatbots can also advertise their use of Layer8 as a
        competitive advantage to users that DO NOT want to share their
        identifying information by default. There are many notable examples of
        online platforms abusing user information. Three examples illustrate the
        point: in 2023 Amazon was fined $25 million for violating children's
        privacy; Meta, $1.3 Billion for violating E.U. Data Privacy Rules; and
        Apple fined €8M in a French privacy case. These companies have the
        resources to absorb such legal actions but small start-ups do not. It is
        to this niche that Layer8 provides its biggest value add. By anonymizing
        and guaranteeing the anonymity of a user’s data, a small startup or
        research team can offload the liability of data protection to Layer8
        allowing them to focus on getting their technology out to market. In
        addition to providing an end-to-end anonymization service, Layer8 will
        also be able to verify a user's credentials (e.g., email addresses,
        phone numbers, etc.) and then store this information in the form of
        zero-knowledge proofs. These proofs, in turn, can be selectively
        forwarded to different servers thereby providing a self-sovereign
        identity service to users that is extremely hack resilient. That is,
        were Layer8’s servers to be hacked, not but zero-knowledge proofs would
        be found and these proofs, by definition, contain zero-knowledge.

        <h3>Globe & Citizen: News of Our World (G&C: NOW)</h3>
        Globe & Citizen itself is going to be the first Service Provider to sign
        up for Layer8’s services to realize our flagship offering, Globe &
        Citizen: News of Our World. By leveraging Layer8’s anonymization
        capabilities, this unique news media web application is pioneering
        several important innovations. Unlike other news websites, G&C: NOW will
        make permissioned, anonymous, user metadata available for opensource
        analysis and scrutiny. Thanks to Layer8, all users can be assured that
        they are acting as an anonymous avatar and only sharing that data that
        they are comfortable sharing. Just like in the real world, where
        reputation matters, avatars too can build a reputation even if
        anonymous. By aggregating and publishing permissioned user metadata,
        users are—in exchange for enjoying free content—lending credibility to
        that which they believe is true through their actions, reactions, and
        other online behaviours. Once aggregated and analyzed, this permissioned
        metadata will provide one of the clearest signals as to truth and
        falsehood on the Internet. For example, imagine a piece of content
        ‘liked’ and ‘shared’ 1,000,000 times by 50 users, each of whom has been
        active on the platform for only one day. This is very likely
        manipulation, misinformation, and/or fake news. In contrast, something
        with only 1,000 ‘likes’, by 1,000 users each of whom has a verified cell
        phone number and telephone number is much more likely to be true. By
        publishing this metadata freely, Globe & Citizen intends to make its
        small contribution to humanity’s never ending attempt to understanding
        itself. Just as various entities compete to understand the weather,
        Globe & Citizen wants to make enough permissioned user metadata
        available such that agencies, academics, interested dabblers, and more
        can observe sociological phenomenon and propose models, theories,
        interpretations, and predictions. Unlike most other news media
        organizations, G&C: NOW, focuses on Prediction Journalism which, much
        like Investigative Journalism, requires an in-depth understanding of a
        subject so that evidence can be integrated into a unified narrative. In
        contrast, however, Prediction Journalism does not focus on exposing
        wrongdoing, corruption, or scandal. Rather, it has the primary focus of
        modeling, understanding, and predicting so that future events can be
        anticipated. Humans, as the complex adaptive systems we all are, must
        inevitably, take in information from our surroundings, process it, and
        with bounded rationality, predict its implications. In other words, we
        are all constantly attempting to absorb the news of our world and act
        accordingly. Unfortunately, as humans, we and the institutions we build
        all lie—constantly— to ourselves and to others and this is why Globe &
        Citizen divests from itself the sole responsibility of infallibly
        parsing truth from falsehood. Rather, all is information with a utility
        and relevance proportional to its predictive potential – not subjective
        veracity.

